public class MatrizUnidimensional7 {
    public static void main(String[] args) {

        // 3. Utilizando o comando for mostre a tabuada do 5 na tela.

        int i, resultado;

        for (i = 0; i <= 10; i++) {

            resultado = 5 * i;

            System.out.println("5 x " + i + "=" + resultado);

        }

    }
}
