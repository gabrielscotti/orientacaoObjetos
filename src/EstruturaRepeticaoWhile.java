public class EstruturaRepeticaoWhile {

    public static void main(String[] args) {
        // Estrutura de Repetição While

        // Início de laço de Repetição
        int i = 0;

        // Fim do laço de Repetição
        while (i <= 10) {

            System.out.println("Número de Repetição" + i);

            // Incremento
            i++;

        }
    }
}
